package com.belk.pep.form;

public class PepDetailsHistory {
    
    private String historyStatus;
    private String updateDate;
    private String pepUpdatedBy;
    private String pepOrinNumber;
    private String createdOn;
    private String createdBy;
    private String updatedStatus;
    
    
    /**
	 * @return the pepUpdatedBy
	 */
	public String getPepUpdatedBy() {
		return pepUpdatedBy;
	}
	/**
	 * @param pepUpdatedBy the pepUpdatedBy to set
	 */
	public void setPepUpdatedBy(String pepUpdatedBy) {
		this.pepUpdatedBy = pepUpdatedBy;
	}
	public String getHistoryStatus() {
        return historyStatus;
    }
    public void setHistoryStatus(String historyStatus) {
        this.historyStatus = historyStatus;
    }
    public String getUpdateDate() {
        return updateDate;
    }
    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }
	/**
	 * @return the pepOrinNumber
	 */
	public String getPepOrinNumber() {
		return pepOrinNumber;
	}
	/**
	 * @param pepOrinNumber the pepOrinNumber to set
	 */
	public void setPepOrinNumber(String pepOrinNumber) {
		this.pepOrinNumber = pepOrinNumber;
	}
	/**
	 * @return the createdOn
	 */
	public String getCreatedOn() {
		return createdOn;
	}
	/**
	 * @param createdOn the createdOn to set
	 */
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}
	/**
	 * @return the createdBy
	 */
	public String getCreatedBy() {
		return createdBy;
	}
	/**
	 * @param createdBy the createdBy to set
	 */
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}
	/**
	 * @return the updatedStatus
	 */
	public String getUpdatedStatus() {
		return updatedStatus;
	}
	/**
	 * @param updatedStatus the updatedStatus to set
	 */
	public void setUpdatedStatus(String updatedStatus) {
		this.updatedStatus = updatedStatus;
	}
	
	

}
