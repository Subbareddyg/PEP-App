package com.belk.pep.attributes;

public class OmniSizeBean {

    private String id;
    private String size;
    
    public OmniSizeBean(String id, String size) {
        this.id = id;
        this.size = size;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
    
    
}
